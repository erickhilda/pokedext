/**
 * Converts a value from decimeters to meters.
 * @param decimeters The value in decimeters.
 * @returns The equivalent value in meters.
 */
export function convertDecimeterToMeter(decimeters: number): number {
  return decimeters / 10;
}

/**
 * Converts a value from hectograms to kilograms.
 * @param hectograms The value in hectograms.
 * @returns The equivalent value in kilograms.
 */
export function convertHectogramsToKilograms(hectograms: number): number {
  return hectograms / 10;
}
